package com.sun.j2ee.blueprints.taglibs.smart;

import javax.servlet.jsp.JspTagException;
import javax.servlet.jsp.tagext.BodyContent;
import javax.servlet.jsp.tagext.BodyTagSupport;


/**
 * DOCUMENT ME!
 *
 * @author $Author: albandri $
 * @version $Revision: 350 $
 *
 * @since $Date: 2010-09-14 01:50:19 +0200 (mar., 14 sept. 2010) $
 */
public class OptionTag extends BodyTagSupport {

    private static final long serialVersionUID = 1L;
    String value = "";

    /**
     * DOCUMENT ME!
     *
     * @param v DOCUMENT ME!
     */
    public void setValue(final String v) {

        this.value = v;

    }

    /**
     * DOCUMENT ME!
     *
     * @return DOCUMENT ME!
     *
     * @throws JspTagException DOCUMENT ME!
     */
    @Override
    public int doAfterBody() throws JspTagException {

        final SelectTag selectTag = (SelectTag) findAncestorWithClass(this, SelectTag.class);
        final BodyContent bc = getBodyContent();
        final String text = bc.getString();

        if ((this.value != null) && !this.value.trim().equals("")) {

            if ((text != null) && !text.trim().equals("")) {

                selectTag.putOption(this.value, ((text != null) && !text.trim().equals("")) ? text : this.value);

            } else {

                selectTag.putOption(this.value, this.value);

            }

        }

        bc.clearBody();

        return SKIP_BODY;

    }

}
